package com.example.micontentprovider;

public class Asignatura {
    private int id;
    private String nombre;
    public Asignatura(){}
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
